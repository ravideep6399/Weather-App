import 'package:flutter/material.dart';

// AppColors class to define a set of commonly used colors in the app
class AppColors {
  // Base color for backgrounds and primary elements
  static const Color baseColor = Colors.white;

  // Black color for text and icons
  static const Color black = Colors.black;

  // Color for error messages and indicators
  static const Color error = Colors.red;

  // Background color for circular elements
  static const Color circleback = Color.fromARGB(140, 198, 198, 198);

  // Color for shadows to give depth to elements
  static const Color shadow = Color.fromARGB(156, 0, 0, 0);

  // Fade color for translucent overlays
  static const Color fade = Color.fromARGB(89, 0, 0, 0);

  // Transparent color for invisible elements
  static const Color transparent = Colors.transparent;
}
